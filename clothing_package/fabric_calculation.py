def calculate_fabric_for_jacket(size):
    # рассчет расхода ткани на пиджак для заданного размера
    fabric_needed = size * 2.5
    return fabric_needed

def calculate_fabric_for_trousers(size):
    # рассчет расхода ткани на брюки для заданного размера
    fabric_needed = size * 1.5
    return fabric_needed

def calculate_fabric_for_suit(size):
    # рассчет расхода ткани на костюм-тройку для заданного размера
    fabric_needed = size * 4.5
    return fabric_needed